package dev.appsody.starter;

public class Credentials {
    public Credentials() {
    }
    public String UID;
    public String PASS;

    public String getUID() {
        return UID;
    }

    public void setUID(String uID) {
        UID = uID;
    }

    public String getPASS() {
        return PASS;
    }

    public void setPASS(String pASS) {
        PASS = pASS;
    }
}